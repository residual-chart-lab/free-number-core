#!/usr/bin/env python3
"""Integrate the 2026-07-12 audit amendments into their parent documents.

The script is deliberately idempotent and fails loudly when an expected source
anchor is absent. It is intended to run in GitHub Actions on the audit branch.
"""

from __future__ import annotations

from pathlib import Path
import shutil

ROOT = Path(__file__).resolve().parents[1]


def read(rel: str) -> str:
    return (ROOT / rel).read_text(encoding="utf-8")


def write(rel: str, text: str) -> None:
    path = ROOT / rel
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def replace_between(text: str, start: str, end: str, replacement: str, *, label: str) -> str:
    if replacement in text:
        return text
    try:
        i = text.index(start)
        j = text.index(end, i)
    except ValueError as exc:
        raise RuntimeError(f"Could not locate integration anchors for {label}") from exc
    return text[:i] + replacement + text[j:]


def integrate_local_closure() -> None:
    rel = "paper/free_numbers.tex"
    text = read(rel)
    start = r"\section{Local Closure \texorpdfstring{$F_1$}{F1}}"
    end = r"\section{Local Charts and Non-Identical Copies}"
    replacement = r"""\section{Local Closure \texorpdfstring{$F_1$}{F1}}

We now construct the local closure as an explicit associative algebra rather
than assuming a four-dimensional multiplication table in advance.

Let
\[
U=\operatorname{span}_{\mathbb R}\{u,v\}
\]
be a two-dimensional real vector space, and let $T(U)$ be its unital tensor
algebra. Let $I$ be the two-sided ideal generated by
\[
u^2+1,\qquad v^2+1,\qquad uv+vu.
\]
Define
\[
\boxed{
F_1:=T(U)/I.
}
\]
Because $T(U)$ is associative and $I$ is a two-sided ideal, $F_1$ is a unital
associative real algebra. The defining relations in the quotient are
\[
u^2=v^2=-1,\qquad uv=-vu.
\]
This is the real Clifford algebra $\mathrm{Cl}_{0,2}$ in a two-generator
presentation. Set
\[
I:=u,\qquad J:=v,\qquad K:=uv.
\]
The symbols $I,J,K$ are the quaternionic pure directions; only $u,v$ are
primitive generators.

\begin{proposition}[Spanning form]
Every element of $F_1$ is a real linear combination of
\[
1,\qquad u,\qquad v,\qquad uv.
\]
In particular, $\dim_{\mathbb R}F_1\le4$.
\end{proposition}

\begin{proof}
Every tensor word is a word in $u,v$. Use $vu=-uv$ to move every $u$ to the
left of every $v$, and use $u^2=v^2=-1$ to reduce both exponents modulo two.
Every word therefore reduces, up to sign, to one of $1,u,v,uv$.
\end{proof}

\begin{proposition}[Local quaternionic form]
The associative local closure is isomorphic, as a real algebra, to the
quaternion algebra:
\[
\boxed{
F_1=T(U)/(u^2+1,\ v^2+1,\ uv+vu)
\cong_{\mathbb R\text{-alg}}\mathbb H.
}
\]
\end{proposition}

\begin{proof}
Define a unital algebra homomorphism $\phi:T(U)\to\mathbb H$ by
\[
\phi(u)=i,\qquad \phi(v)=j.
\]
The generators of $I$ lie in $\ker\phi$, so $\phi$ descends to
\[
\overline\phi:F_1\longrightarrow\mathbb H.
\]
Its image contains $1,i,j,ij=k$, hence it is surjective. Therefore
$\dim_{\mathbb R}F_1\ge4$. The spanning proposition gives the reverse
inequality, so $\dim_{\mathbb R}F_1=4$ and the surjection is an isomorphism.
\end{proof}

The third pure direction is not a third primitive generator. It is the product
$uv$, equivalently the bivector direction in $\Lambda^2U$. This local theorem
does not identify different local copies globally; global chart identity
remains governed by admissible transitions and residual structure.

"""
    new = replace_between(text, start, end, replacement, label=rel)
    write(rel, new)


def integrate_note12() -> None:
    parent = ROOT / "notes/12-certified-red-restricted-weak-confluence.md"
    repair = ROOT / "notes/12a-certified-red-confluence-repair.md"
    archive = ROOT / "archive/notes/12-certified-red-restricted-weak-confluence-original.md"

    original = parent.read_text(encoding="utf-8")
    if not archive.exists():
        archive.parent.mkdir(parents=True, exist_ok=True)
        archive.write_text(original, encoding="utf-8")

    corrected = repair.read_text(encoding="utf-8")
    corrected = corrected.replace(
        "# Note 12a — Certified Red Confluence Repair",
        "# Note 12 — Certified Red: Corrected Theorem Split",
        1,
    )
    corrected += (
        "\n\n---\n\n## Archival location\n\n"
        "The superseded unconditional version is preserved at "
        "`archive/notes/12-certified-red-restricted-weak-confluence-original.md`.\n"
    )
    parent.write_text(corrected, encoding="utf-8")


def integrate_core07_markdown() -> None:
    rel = "core/07-length-four-cross-boundary-propagation.md"
    text = read(rel)
    old = r"""Since

\[

\dim RR=25,
\]

its kernel has dimension nine. By the multiplicity-free decomposition of \(RR\), this kernel is exactly

\[

\boxed{
\ker\bigl(D_{13}|_{RR}\bigr)=V_4=S^4_0V.
}
\]"""
    new = r"""Since

\[
\dim RR=25,
\]

its kernel has dimension nine. The dimension count alone does not identify the kernel: \(V_0\oplus V_1\oplus V_2\) also has dimension nine. The identification uses the full depth-two profile.

Compression and every depth-one response vanish on \(RR\), and \(D_{13}\) is the only depth-two component that can be nonzero there. Hence

\[
\ker\bigl(D_{13}|_{RR}\bigr)
=F_2^{(4)}\cap RR.
\]

The independently verified length-four subspace equality is

\[
F_2^{(4)}=S^4_0V,
\]

and \(S^4_0V\subset RR\). Therefore

\[
\boxed{
\ker\bigl(D_{13}|_{RR}\bigr)=S^4_0V=V_4.
}
\]

The rank calculation \(25-16=9=\dim V_4\) is a consistency check, not the sole identification argument."""
    if new not in text:
        if old not in text:
            raise RuntimeError(f"Could not locate original kernel-identification paragraph in {rel}")
        text = text.replace(old, new, 1)
    write(rel, text)


